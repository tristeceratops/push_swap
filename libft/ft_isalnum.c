int	ft_isalnum(int c)
{
	int	ret_value;

	ret_value = 0;
	if (c >= 48 && c <= 57)
		ret_value = 1;
	if (c >= 65 && c <= 90)
		ret_value = 1;
	if (c >= 97 && c <= 122)
		ret_value = 1;
	return (ret_value);
}
